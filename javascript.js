$(window).load(function () {
    $('.trigger_popup_fricc_deezer').click(function(){
       $('.hover_bkgr_fricc_deezer').show();
    });
    $('.hover_bkgr_fricc_deezer').click(function(){
        $('.hover_bkgr_fricc_deezer').hide();
    });
    $('.popupCloseButton').click(function(){
        $('.hover_bkgr_fricc_deezer').hide();
    });
});
